// van Wijk 2022 Med Teach
// Get the modal
var modalwijk2022mt = document.getElementById("wijk-2022-mt");

// Get the button that opens the modal
var btnwijk2022mt = document.getElementById("wijk-2022-mt-button");

// Get the button that closes the modal
var spanwijk2022mt = document.getElementById("close-button");

// When the user clicks on the button, open the modal
btnwijk2022mt.onclick = function() {
    modalwijk2022mt.style.display = "block";
}

// When the user clicks on <span> (x), close the modal
spanwijk2022mt.onclick = function() {
    modalwijk2022mt.style.display = "none";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
  if (event.target == modalwijk2022mt) {
    modalwijk2022mt.style.display = "none";
  }
} 

